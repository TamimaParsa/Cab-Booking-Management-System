    private void tableIntracityMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableIntracityMouseClicked
        // TODO add your handling code here:
        int index = tableIntracity.getSelectedRow();
        TableModel model = tableIntracity.getModel();
        String id = model.getValueAt(index, 0).toString();
        DriverPk = Integer.parseInt(id);
        
        String driver = model.getValueAt(index, 1).toString();
        txtDriver.setText(driver);
        
        String city = model.getValueAt(index, 2).toString();
        txtCity.setText(city);
        
        String source = model.getValueAt(index, 3).toString();
        txtSource.setText(source);
        
        String destination = model.getValueAt(index, 4).toString();
        txtDestination.setText(destination);
        
        String car = model.getValueAt(index, 5).toString();
        txtCar.setText(car);
        
        String price = model.getValueAt(index, 6).toString();
        txtPrice.setText(price);
        
        String phone = model.getValueAt(index, 7).toString();
        txtPhone.setText(phone);
        
        btnAdd.setEnabled(false);
        btnUpdate.setEnabled(true);
    }//GEN-LAST:event_tableIntracityMouseClicked

    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) tableIntracity.getModel();
        try{
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select *from intracityDriver");
            while(rs.next()){
                model.addRow(new Object[]{rs.getString("driver_pk"),rs.getString("drivername"),rs.getString("city"),rs.getString("source"),rs.getString("destination"),rs.getString("car"),rs.getString("price"),rs.getString("phoneNumber")});
            }
        }
        catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
                } 
        btnUpdate.setEnabled(false);
    }//GEN-LAST:event_formComponentShown

