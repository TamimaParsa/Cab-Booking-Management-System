    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        if (DriverPk == 0) {
        JOptionPane.showMessageDialog(this, "Please select a driver to delete.");
        return;
    }
        int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this driver?", "Confirm Deletion", JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            try {
                Connection con = ConnectionProvider.getCon();
                PreparedStatement ps = con.prepareStatement("DELETE FROM intracityDriver WHERE driver_pk = ?");
                ps.setInt(1, DriverPk);
                ps.executeUpdate();

                JOptionPane.showMessageDialog(this, "Intracity Driver deleted successfully.");

                // Reset fields
                txtDriver.setText("");
                txtCity.setText("");
                txtSource.setText("");
                txtDestination.setText("");
                txtCar.setText("");
                txtPrice.setText("");
                txtPhone.setText("");

                // Refresh table
                DefaultTableModel model = (DefaultTableModel) tableIntracity.getModel();
                model.setRowCount(0); // clear existing rows
                formComponentShown(null); // reload data

                btnAdd.setEnabled(true);
                btnUpdate.setEnabled(false);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e);
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

