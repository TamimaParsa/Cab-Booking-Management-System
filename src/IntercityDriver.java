    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String drivername= txtDriver.getText();
        String source= txtSource.getText();
        String destination = txtDestination.getText();
        String car = txtCar.getText();
        String price= txtPrice.getText();
        String phone = txtPhone.getText();
      
        if(validateFields()){
            JOptionPane.showMessageDialog(null, "All fields are required");
        }
        else{
            try{
                Connection con = ConnectionProvider.getCon();
                PreparedStatement ps = con.prepareStatement("insert into intercityDriver (drivername,source,destination,car,price,phoneNumber) values(?,?,?,?,?,?)");
                ps.setString(1,drivername);
                ps.setString(2,source);
                ps.setString(3,destination);
                ps.setString(4,car);
                ps.setString(5,price);
                ps.setString(6,phone);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Intercity Driver added successfully");
                setVisible(true);
                txtDriver.setText("");
                txtSource.setText("");
                txtDestination.setText("");
                txtCar.setText("");
        txtPrice.setText("");
        txtPhone.setText("");
                //new IntercityDriver().setVisible(true);
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_btnAddActionPerformed

