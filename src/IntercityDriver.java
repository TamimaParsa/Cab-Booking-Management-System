    private void tableIntercityMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableIntercityMouseClicked
        // TODO add your handling code here:
        int index = tableIntercity.getSelectedRow();
        TableModel model = tableIntercity.getModel();
        String id = model.getValueAt(index, 0).toString();
        DriverPk = Integer.parseInt(id);
        
        String driver = model.getValueAt(index, 1).toString();
        txtDriver.setText(driver);
        
        String source = model.getValueAt(index, 2).toString();
        txtSource.setText(source);
        
        String destination = model.getValueAt(index, 3).toString();
        txtDestination.setText(destination);
        
        String car = model.getValueAt(index, 4).toString();
        txtCar.setText(car);
        
        String price = model.getValueAt(index, 5).toString();
        txtPrice.setText(price);
        
        String phone = model.getValueAt(index, 6).toString();
        txtPhone.setText(phone);
        
        btnAdd.setEnabled(false);
        btnUpdate.setEnabled(true);
    }//GEN-LAST:event_tableIntercityMouseClicked

