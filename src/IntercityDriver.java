    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) tableIntercity.getModel();
        try{
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select *from intercityDriver");
            while(rs.next()){
                model.addRow(new Object[]{rs.getString("driver_pk"),rs.getString("drivername"),rs.getString("source"),rs.getString("destination"),rs.getString("car"),rs.getString("price"),rs.getString("phoneNumber")});
            }
        }
        catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
                } 
        btnUpdate.setEnabled(false);
    }//GEN-LAST:event_formComponentShown

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        if (DriverPk == 0) {
        JOptionPane.showMessageDialog(this, "Please select a driver to delete.");
        return;
    }
        int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this driver?", "Confirm Deletion", JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            try {
                Connection con = ConnectionProvider.getCon();
                PreparedStatement ps = con.prepareStatement("DELETE FROM intercityDriver WHERE driver_pk = ?");
                ps.setInt(1, DriverPk);
                ps.executeUpdate();

                JOptionPane.showMessageDialog(this, "Intercity Driver deleted successfully.");

                // Reset fields
                txtDriver.setText("");
                txtSource.setText("");
                txtDestination.setText("");
                txtCar.setText("");
                txtPrice.setText("");
                txtPhone.setText("");

                // Refresh table
                DefaultTableModel model = (DefaultTableModel) tableIntercity.getModel();
                model.setRowCount(0); // clear existing rows
                formComponentShown(null); // reload data

                btnAdd.setEnabled(true);
                btnUpdate.setEnabled(false);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e);
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

